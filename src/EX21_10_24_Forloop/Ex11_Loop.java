package EX21_10_24_Forloop;

public class Ex11_Loop {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            if (i == 5) {
                System.out.println("Five");

            }else {
                System.out.println(i);
                //o,1,2,3,4,Five,6,7,8,9
            }

        }
    }
}
