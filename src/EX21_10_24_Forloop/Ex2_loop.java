package EX21_10_24_Forloop;

public class Ex2_loop {
    public static void main(String[] args) {
        for(int _1 = 0; _1 <=10;_1++)
        {
            System.out.println(_1);//11 times run and will print 0 to 10
        }
        for(int i = 0;i<=10;++i){
            System.out.println(i);//whether pre or post increment value did not change

        }
    }
}
