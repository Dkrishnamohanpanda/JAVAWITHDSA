public class Ex5_18_10_24 {
    public static void main(String[] args) {
        int itemcode = 001;
        switch (itemcode){
            case 001 -> System.out.println("001");
            case 002 -> System.out.println("001");
            case 003 -> System.out.println("001");
            default -> System.out.println("default");
        }
    }
}
